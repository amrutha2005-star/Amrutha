// String
let str = "Hello, World!";
console.log("Value of str:", str); // "Hello, World!"
console.log("Type of str:", typeof str); // string

// Number
let num = 42;
console.log("Value of num:", num); // 42
console.log("Type of num:", typeof num); // number

// Boolean
let bool = true;
console.log("Value of bool:", bool); // true
console.log("Type of bool:", typeof bool); // boolean

// Null
let nul = null;
console.log("Value of nul:", nul); // null
console.log("Type of nul:", typeof nul); // object (This is a known JavaScript quirk)

// Undefined
let undef;
console.log("Value of undef:", undef); // undefined
console.log("Type of undef:", typeof undef); // undefined

// Symbol
let sym = Symbol('unique');
console.log("Value of sym:", sym); // Symbol(unique)
console.log("Type of sym:", typeof sym); // symbol
